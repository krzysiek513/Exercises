package Ch2_Intro;

public class ShapesWithAsterisks {
    public static void main(String[] args) {
        System.out.println("********   ***      *       *    ");
        System.out.println("*      *  *   *    ***     * *   ");
        System.out.println("*      * *     *  *****   *   *  ");
        System.out.println("*      * *     *    *    *     * ");
        System.out.println("*      * *     *    *   *       *");
        System.out.println("*      * *     *    *    *     * ");
        System.out.println("*      * *     *    *     *   *  ");
        System.out.println("*      *  *   *     *      * *   ");
        System.out.println("********   ***      *       *    ");

        System.out.printf("*%n**%n***%n****%n*****%n");

        System.out.println("*");
        System.out.println("***");
        System.out.println("*****");
        System.out.println("****");
        System.out.println("**");

        System.out.print("*");
        System.out.print("***");
        System.out.print("*****");
        System.out.print("****");
        System.out.println("**");

        System.out.print("*");
        System.out.println("***");
        System.out.println("*****");
        System.out.print("****");
        System.out.println("**");

        System.out.printf("%s%n%s%n%s%n", "*", "***", "*****");
    }
}
